import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-medium transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground",
        secondary: "border-transparent bg-secondary text-secondary-foreground",
        destructive: "border-transparent bg-destructive text-destructive-foreground",
        outline: "text-foreground border-border",
        // Status badges
        success: "border-transparent bg-success/15 text-success",
        warning: "border-transparent bg-warning/15 text-warning-foreground",
        info: "border-transparent bg-info/15 text-info",
        // Tier badges
        free: "border-transparent bg-muted text-muted-foreground",
        premium: "border-transparent gradient-premium text-white",
        ultra: "border-transparent gradient-ultra text-black",
        // Job badges
        new: "border-transparent bg-accent/15 text-accent font-semibold",
        hot: "border-transparent bg-destructive/15 text-destructive font-semibold",
        remote: "border-transparent bg-info/15 text-info",
        fullTime: "border-transparent bg-primary/10 text-primary",
        partTime: "border-transparent bg-warning/15 text-warning-foreground",
        contract: "border-transparent bg-premium/15 text-premium",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  },
);

export interface BadgeProps extends React.HTMLAttributes<HTMLDivElement>, VariantProps<typeof badgeVariants> {
  children?: React.ReactNode;
}

function Badge({ className, variant, children, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props}>{children}</div>;
}

export { Badge, badgeVariants };
